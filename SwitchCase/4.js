num1 = 20
num2 = 30
op = "/"

switch(op){
    case "+":
        console.log(num1 + num2)
    break;

    case "-":
        console.log(num1 - num2)
    break;

    case "*":
        console.log(num1 * num2)
    break;

    case "/":
        console.log(num1 / num2)
    break;

    default:
        console.log("operador invalido")
    break;
}